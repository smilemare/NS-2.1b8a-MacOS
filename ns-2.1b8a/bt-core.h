/*****************************************************************
 * The Baseband Class:
 * This class controls the trasmitting/receiving of packets
 * and implements most of the baseband functionality 
 * of the simulator: 
 * (1) State machine of the link controller. Bluetooth Specs v1.0A page 98
 * (2) Frequency hopping kernel. 
 * (3) Inquiry and paging procedures as in Bluetooth Specs.
 * (4) Location dependent error model
 * (5) Some HCI commands
 ****************************************************************/


class Baseband : public BiConnector {

	/* Handler classes */
	friend class CLKNHandler;		
	friend class CLKHandler;		 
	friend class RecvHandler;
	friend class TxHandler;
	friend class InterfererHandler;
	friend class TDDHandler;

	/* Addresses and access codes */
	int 	bd_addr_;
	int 	master_addr_;
	int 	giac_;
	int 	dac_;
	int 	page_addr_;

	vector<FHS_payload *> 	addr_vec_;
	uchar	active_list_[8];

	/* Clocks: native, master's, frozen and estimate */	
	long 	clkn_; 
	long	clk_; 
	long	clkf_; 
	long	clke_;

	/* Transmission timer */
	int 	txTimer_;

	/* Timers/counters related to inquiry/inquiry scan */
	long 	inqTimer_;
	long 	inqscanTimer_;
	long 	inqbackoffTimer_;
	long 	inqrespTimer_;
	int	Nfhs_;
	int 	numResponses_;

	/* Timers/counters related to page/page scan */
	long 	pageTimer_;
	long 	pagescanTimer_;
	long 	pagerespTimer_;
	long 	newConnectionTimer_;
	int 	Nmr_; 
	int	Nsr_;
	uchar 	Nsr_incr_offset_;
	int  	lastPaged_;

	/* Common variables for page and inquiry */
	int 		numId_; 
	int		numTrainsSent_;
	train_type      trainType_;
	uchar 		freeze_;


	/* State Variables */
	state_type      state_, prev_state_;
	tdd_state_type  tddState_;
	uchar		polled_;
	
	LMP** 		linkq_;

	/* Pointer to instance of Bluetooth Host (used for HCI exchanges) */
	BTHost* 	bthost_;

	/* Queue of connection requests */
	queue<ConnectionRequest*>* requestQ_;			// Check

	/* Collision detection and other receiver variables */
	PacketQueue* 	interfererQ_;
	PacketQueue* 	potinterfererQ_;

	double 		recv_start_;
	int 		recv_freq_;

	/* Simulation Events */
	Event 		clkn_ev_;		
	Event 		clk_ev_;		
	Event 		req_ev_;		
	Event 		tdd_ev_;		

	CLKNHandler		clkn_handler_;		
	CLKHandler		clk_handler_; 
	RecvHandler		recv_handler_;
	InterfererHandler	int_recv_handler_;
	TxHandler 		tx_handler_;
	TDDHandler 		tdd_handler_;

	static int 		firsttime_;
	int 			namTrace_;
	
	int 	seed_;
	int 	masterIndex_;
	int 	isOffset_;
	double	xpos_;
	double  ypos_;
	double  interArrival_;
	uchar 	am_addr_;
	uchar 	new_am_addr_;
	int 	numACLlinks_;

	/* Identifies currently used clock ie, CLK or CLKN */
	clock_type tx_clock_;
public:
	Baseband();
	void 	sendUp (Packet* p, Handler* h);
	int 	command (int, const char*const* );
	int 	recv_filter (Packet*);

	double 	calcLossProb (bt_packet_type, double); 
	void 	getAM_ADDR();
	void 	sendIDPacket(int);
	void 	sendFHSPacket();
	void 	sendPOLLPacket(uchar);
	void 	sendNULLPacket(uchar);
	void 	sendPacket (Packet* p);
	void 	sendHCI_Inquiry_Result(){}
	void 	sendPageResult() {}
	void 	sendPageSuccess(int) {}
	void 	sendPollResponse() {}
	void 	handleInqRespTO();

	void	start();
	Packet* 	schedule_next() {}

	//	HCI_commands
	void 	HCI_Inquiry (long, int);
	void	HCI_Create_Connection (int, int);

	
	void 	updateInqPageParam();
	void 	initInqPageParam();
	void 	changeState(state_type);
	uchar 	FH_kernel(int CLK, int CLK_frozen, FH_sequence_type FH_seq_type_t,
                int BD_addr); 
	uchar 	ADD_mod32(uchar ip1, uchar ip2);
	uchar 	EXOR_5(uchar ip1, uchar ip2);
	uchar 	EXOR_9(uint ip1, uint ip2);
	uchar 	PERM(uchar ip1, uchar ip2, uint ip3);
	uchar 	ADD_mod79(uchar ip1, uchar ip2, uchar ip3, uchar ip4);
	void  	BFLY(uchar ctrl, uchar& ip1, uchar& ip2);
};

